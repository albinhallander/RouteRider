#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'node:fs';
import { execSync } from 'node:child_process';
import { join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..');
const CLAUDE_MD = join(ROOT, 'CLAUDE.md');
const BEGIN = '<!-- AUTO:BEGIN';
const END = '<!-- AUTO:END -->';
const IGNORE_DIRS = new Set(['node_modules', '.git', 'dist', 'coverage', '.vite']);
const TEST_RE = /\.(test|spec)\.(js|jsx|ts|tsx)$/;

if (!existsSync(CLAUDE_MD)) process.exit(0);
const original = readFileSync(CLAUDE_MD, 'utf8');
const beginIdx = original.indexOf(BEGIN);
const endIdx = original.indexOf(END);
if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) process.exit(0);

const pkg = JSON.parse(readFileSync(join(ROOT, 'package.json'), 'utf8'));

function walk(dir, depth = 0, max = 2, out = []) {
  if (depth > max) return out;
  for (const name of readdirSync(dir).sort()) {
    if (IGNORE_DIRS.has(name) || name.startsWith('.')) continue;
    const full = join(dir, name);
    const rel = relative(ROOT, full).split(sep).join('/');
    const s = statSync(full);
    if (s.isDirectory()) {
      out.push(`${'  '.repeat(depth)}- ${rel}/`);
      walk(full, depth + 1, max, out);
    } else {
      out.push(`${'  '.repeat(depth)}- ${rel}`);
    }
  }
  return out;
}

function findTests(dir, out = []) {
  if (!existsSync(dir)) return out;
  for (const name of readdirSync(dir)) {
    if (IGNORE_DIRS.has(name)) continue;
    const full = join(dir, name);
    const s = statSync(full);
    if (s.isDirectory()) findTests(full, out);
    else if (TEST_RE.test(name)) out.push(relative(ROOT, full).split(sep).join('/'));
  }
  return out;
}

let sha = 'unknown';
try { sha = execSync('git rev-parse --short HEAD', { cwd: ROOT }).toString().trim(); } catch {}

const tree = walk(ROOT).join('\n');
const scripts = Object.entries(pkg.scripts || {}).map(([k, v]) => `- \`${k}\`: \`${v}\``).join('\n') || '_none_';
const deps = Object.entries(pkg.dependencies || {}).map(([k, v]) => `- ${k} ${v}`).join('\n') || '_none_';
const tests = findTests(join(ROOT, 'src'));
const testList = tests.length ? tests.map(t => `- ${t}`).join('\n') : '_no tests yet_';

const block = `${BEGIN} — regenerated by scripts/refresh-claude-md.mjs; do not edit -->

_Last refreshed: ${new Date().toISOString()} · commit ${sha}_

### File tree (top 2 levels)
${tree}

### npm scripts
${scripts}

### Runtime dependencies
${deps}

### Test files (${tests.length})
${testList}

${END}`;

const before = original.slice(0, beginIdx);
const after = original.slice(endIdx + END.length);
const next = `${before}${block}${after}`;

if (next !== original) {
  writeFileSync(CLAUDE_MD, next);
  console.log(`[refresh-claude-md] updated (${tests.length} tests, commit ${sha})`);
} else {
  console.log('[refresh-claude-md] no changes');
}
